def month (number)

    case number
    when 1 
      puts "Jan"
    when 2
      puts "Feb"
    when 3
      puts "Mar"
    when 4
      puts "Apr"
    when 5 
      puts "May"
    when 6
      puts "June"
    when 7
    puts "July"
    when 8
      puts "Aug"
    when 9 
      puts "Sept"
    when 10
      puts "Oct"
    when 11 
      puts "Nov"
    when 12 
      puts "Dec"
    end
    end

    puts month (8)